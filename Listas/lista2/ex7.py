# ### **Exercício 7: Iterando com Índices**
#
# Crie uma lista chamada `frutas` com os elementos `"maçã"`, `"banana"`, `"laranja"`, `"uva"`.
# Use um loop `for` para imprimir cada elemento junto com seu índice correspondente.
frutas = ["maçã", "banana", "laranja", "uva"]

for fruta in frutas:
    print(fruta)