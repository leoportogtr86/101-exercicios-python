# 2. **Operações Matemáticas Simples**: Calcule a soma, subtração, multiplicação e
# divisão de dois números.
def soma(a, b):
    return a + b


def subtracao(a, b):
    return a - b


def divisao(a, b):
    return a / b


def multiplicacao(a, b):
    return a * b


print(soma(10, 5))
print(subtracao(10, 5))
print(divisao(10, 5))
print(multiplicacao(10, 5))
